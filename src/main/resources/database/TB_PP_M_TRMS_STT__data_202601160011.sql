<h4>제3조(개인정보의 제3자 제공)</h4>  
<p>1. 안전원은 정보주체의 개인정보를 원칙적으로 이용자의 개인정보를 제1조(개인정보의 처리 목적)에서 명시한 범위 내에서 처리하며, 이용자의 사전 동의 없이는 본래의 범위를 초과하여 처리하거나 제3자에게 제공하지 않습니다. 다만, 제5호부터 9호까지의 경우는 공공기관의 경우로 한정합니다.</p>
<p>&nbsp;&nbsp;1) 정보주체로부터 별도의 동의를 받는 경우
<br>&nbsp;&nbsp;2) 법률에 특별한 규정이 있는 경우
<br>&nbsp;&nbsp;3) 정보주체 또는 법정대리인이 의사표시를 할 수 없는 상태에 있거나 주소불명 등으로 사전 동의를 받을 수 없는 경우로서 명백히 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;정보주체 또는 제3자의 급박한 생명, 신체, 재산의 이익을 위하여 필요하다고 인정되는 경우
<br>&nbsp;&nbsp;4) 통계작성 및 학술연구 등의 목적을 위하여 필요한 경우로서 특정 개인을 알아 볼 수 없는 형태로 개인정보를 제공하는 경우
<br>&nbsp;&nbsp;5) 개인정보를 목적 외의 용도로 이용하거나 이를 제3자에게 제공하지 아니하면 다른 법률에서 정하는 소관 업무를 수행할 수 없는 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;경우로서 보호위원회의 심의·의결을 거친 경우
<br>&nbsp;&nbsp;6) 조약, 그 밖의 국제협정의 이행을 위하여 외국정보 또는 국제기구에 제공하기 위하여 필요한 경우
<br>&nbsp;&nbsp;7) 범죄의 수사와 공소의 제기 및 유지를 위하여 필요한 경우
<br>&nbsp;&nbsp;8) 법원의 재판업무 수행을 위하여 필요한 경우
<br>&nbsp;&nbsp;9) 형 및 감호, 보호처분의 집행을 위하여 필요한 경우    
</p>
<p>2. 안전원은 개인정보를 제3자에게 제공하는 경우 다음의 항목을 정보주체에게 알린 후 동의를 받겠습니다.</p>
<p>&nbsp;&nbsp;1) 제공받는 자의 성명(법인 또는 단체인 경우에는 그 명칭)과 연락처
<br>&nbsp;&nbsp;2) 제공받는 자의 개인정보 이용 목적, 제공 하는 개인정보의 항목
<br>&nbsp;&nbsp;3) 개인정보를 제공 받는 자의 개인정보 보유 및 이용기간
