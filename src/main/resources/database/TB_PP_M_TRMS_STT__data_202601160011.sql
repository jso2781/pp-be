<p>3. 개인정보 파기 절차 및 방법은 다음과 같습니다.</p>
<p>1) 파기절차
<br>안전원은 파기하여야 하는 개인정보(또는 개인정보파일)에 대해 개인정보 파기계획을 수립하여 파기합니다. 안전원은 파기사유가 발생한 개인정보(또는 개인정보파일)을 선정하고, 안전원의 개인정보 보호책임자의 승인을 받아 개인정보(또는 개인정보파일)을 지체 없이 파기합니다.</p>
<p>2) 파기방법
<br>안전원은 전자적 파일 형태로 기록·저장된 개인정보는 기록을 재생할 수 없도록 파기하며, 종이 문서에 기록·저장된 개인정보는 분쇄기로 분쇄하여 파기합니다.</p>

