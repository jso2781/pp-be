<h4>제9조 (개인정보를 자동으로 수집하는 장치의 설치·운영 및 그 거부에 관한 사항)</h4>
<p>1. 우리 홈페이지는 이용자에게 개인형서비스를 제공하기 위해 이용정보를 저장하고 수시로 불러오는 ‘쿠키(cookies)’를 사용합니다.</p>
<p>2. 쿠키는 웹사이트를 운영하는데 이용되는 서버가 사용자의 컴퓨터 브라우저에게 보내는 소량의 정보이며 이용자들의 PC 컴퓨터내의 하드디스크에 저장 되기도 합니다.
<br>1) 쿠키의 사용목적 : 자주 찾는 서비스를 설정할 수 있도록 하여 이용자에게 최적화된 정보 제공을 위해 사용합니다.
<br>2) 쿠키의 설치 운영 및 거부 : 웹브라우저 상단의 도구 &gt; 인터넷 옵션 &gt; 개인정보 메뉴의 옵션 설정을 통해 쿠키 저장을 거부할 수 있습니다.
<br>3) 쿠키 저장을 거부할 경우 개인용 서비스 이용에 어려움이 발생할 수 있습니다.
</p>
<p>3. 본인의 개인정보 수집 및 이용을 윈치 않으실 경우 본 동의를 거절하실 수 있습니다. 동의를 거절하실 경우에는 서비스 이용이 제한됨을 알려드립니다.</p>

